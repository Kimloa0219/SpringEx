package com.wonju.ex.lesson01.model;

public class Person {
	// 이름, 나이
	private String name;
	private int  age;
	
	public String getName() {
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return this.age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
